#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list	*lstnext;

	if (lst != 0)
	{
		while (*lst != 0)
		{
			lstnext = (*lst)->next;
			ft_lstdelone(*lst, del);
			*lst = lstnext;
		}
	}
}
